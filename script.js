// Retrieve bahasa browser
function getBrowserLanguage() {
  let lang = navigator.language.split('-')[0]; // Split text buat ngambil split pertama untuk bahasa
  return translations[lang] ? lang : "en"; // Default-nya "en"
}

let currentLang = getBrowserLanguage(); // Atur bahasa awal

// Fungsi untuk ganti bahasa
function switchLanguage(lang) {
  currentLang = lang;
  updateContent();
}

// Fungsi untuk update teks
function updateContent() {
  document.querySelectorAll('[data-translate]').forEach(element => {
    const key = element.getAttribute('data-translate');
    element.textContent = translations[currentLang][key];
  });
}

// Jalankan saat halaman dimuat
document.addEventListener('DOMContentLoaded', function() {
  updateContent();
});

function toggleLangMenu() {
    let menu = document.getElementById("langMenu");
    menu.style.display = (menu.style.display === "block") ? "none" : "block";
}

// Tutup menu jika klik di luar
document.addEventListener("click", function(event) {
    let menu = document.getElementById("langMenu");
    let button = document.querySelector(".hamburger-btn");

    if (!menu.contains(event.target) && !button.contains(event.target)) {
        menu.style.display = "none";
    }
});

const cards = document.querySelectorAll('.carousel-card');

const images = [
    ['imgs/carouselroblox1.png', 'imgs/carouselroblox2.png'], // Card 1
    ['imgs/carouselstumbleguys1.png', 'imgs/carouselstumbleguys2.png', 'imgs/carouseltheheck.gif'] // Card 2
];

let indices = [0, 0];

function changeBackground() {
    cards.forEach((card, i) => {
        indices[i] = (indices[i] + 1) % images[i].length; // Ganti ke foto berikutnya
        card.style.backgroundImage = `
            linear-gradient(to bottom, rgba(0, 0, 0, 0.4) 5%, rgba(0, 0, 0, 0) 95%),
            url(${images[i][indices[i]]})`;
    });
}

// Foto pertama diinisiasi
changeBackground();
setInterval(changeBackground, 15000); // x÷1000 detik interval